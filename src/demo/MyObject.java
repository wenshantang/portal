package demo;

import java.io.Serializable;

class MyObject implements Serializable {
	
	/**
	 * 
	 */
	private static final long serialVersionUID = -1534118615941848245L;
	/**
	 * 
	 */
	private String id;
	private String name;
	private String price;
	
	public MyObject(String id, String name, String price) {
		super();
		this.id = id;
		this.name = name;
		this.price = price;
	}
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
 
}